# Contract address (Replace with your deployed contract address)
contract_address = '0xb4356E49aD45E7477e53BE330Df7Ce8F67cb4ca2'
contract = w3.eth.contract(address=contract_address, abi=simple_erc20_abi)

@app.route('/')
def index():
    return render_template('index.html')

@app.route('/balance', methods=['POST'])
def get_balance():
    address = request.form.get('address')
    try:
        balance = contract.functions.balanceOf(address).call()
        return render_template('balance_result.html', address=address, balance=balance)
    except Exception as e:
        flash(f'Error fetching balance: {str(e)}', 'error')
        return redirect(url_for('index'))

@app.route('/transfer', methods=['POST'])
def transfer():
    # Extract data from the form
    to_address = request.form.get('to_address')
    amount = int(request.form.get('amount'))
    from_address = request.form.get('from_address')
    private_key = request.form.get('private_key')

    # Create a transaction
    nonce = w3.eth.get_transaction_count(from_address)
    tx = {
        'nonce': nonce,
        'to': contract_address,
        'gas': 2000000,
        'gasPrice': w3.to_wei('50', 'gwei'),
        'data': contract.functions.transfer(to_address, amount).build_transaction({'chainId': 1, 'gas': 2000000, 'gasPrice': w3.to_wei('50', 'gwei'), 'nonce': nonce})['data']
    }

    # Sign and send the transaction
    signed_tx = w3.eth.account.sign_transaction(tx, private_key)
    tx_hash = w3.eth.send_raw_transaction(signed_tx.rawTransaction)
    flash('Transfer initiated!', 'success')
    return redirect(url_for('index'))

